package Study_00;

public class Study_00_04_Switch {

	public static void main(String[] args) {
		switch(1){
		case 1:
			System.out.println(1);
		case 2:
			System.out.println(2);
		case 3:
			System.out.println(3);
		}
		System.out.println();
		switch(2){
		case 1:
			System.out.println(1);
		case 2:
			System.out.println(2);
		case 3:
			System.out.println(3);
		}
		System.out.println();
		switch(3){
		case 1:
			System.out.println(1);
		case 2:
			System.out.println(2);
		case 3:
			System.out.println(3);
		}
		System.out.println();
		switch(4){
		case 1:
			System.out.println(1);
		case 2:
			System.out.println(2);
		case 3:
			System.out.println(3);
		default : 
			System.out.println("default");
		}

	}

}
